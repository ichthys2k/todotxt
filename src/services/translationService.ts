export function t(
  key: keyof typeof translations['de'],
  lang: Language,
  placeholderVal?: string | number,
  extraPlaceholderVal?: string | number
): string {
  const dictionary = translations[lang] || translations['de'];
  let text = (dictionary as any)[key] || (translations['de'] as any)[key] || String(key);
  
  if (placeholderVal !== undefined) {
    text = text.replace('{val}', String(placeholderVal)).replace('{today}', String(placeholderVal));
  }
  if (extraPlaceholderVal !== undefined) {
    text = text.replace('{overdue}', String(extraPlaceholderVal));
  }
  
  return text;
}
